package MenschEargereDichNicht.model;
/**
 * Das Enum "PlayerColor" repräsentiert die Spielerfarben im Spiel "Mensch Ärgere Dich Nicht".
 * Es enthält die vier Spielerfarben: BLAU, ROT, VIOLETT und ORANGE.
 *
 * Verwendung:
 * - {@code Pocemon(PlayerColor.BLUE, anotherParameter, another Parameter)} für die blaue Spielerfarbe
 * - {@code Pocemon(PlayerColor.RED, anotherParameter, another Parameter)} für die rote Spielerfarbe
 * - {@code Pocemon(PlayerColor.PURPLE, anotherParameter, another Parameter)} für die violette Spielerfarbe
 * - {@code Pocemon(PlayerColor.ORANGE, anotherParameter, another Parameter)} für die orangene Spielerfarbe
 *  @author Anas Aji
 *  @version 1.0
 */
public enum PlayerColor {

    BLUE, RED, PURPLE, ORANGE
}
